// Function to load language
const loadLanguage = () => {
  const savedLang = localStorage.getItem("selectedLang") || "ar";
  const selectedLang = translations[savedLang];
  const langToggle = document.getElementById("language-toggle");

  // Update navigation
  document.querySelectorAll(".navbar-nav .nav-link").forEach((link, index) => {
    if (index === 0) link.textContent = selectedLang.nav.home;
    if (index === 1) link.textContent = selectedLang.nav.about;
    if (index === 2) link.textContent = selectedLang.nav.services;
    if (index === 3) link.textContent = selectedLang.nav.team;
    if (index === 4) link.textContent = selectedLang.nav.contact;
  });
  document.querySelectorAll(".footer-links ul li a").forEach((link, index) => {
    if (index === 0) link.textContent = selectedLang.nav.home;
    if (index === 1) link.textContent = selectedLang.nav.about;
    if (index === 2) link.textContent = selectedLang.nav.services;
    if (index === 3) link.textContent = selectedLang.nav.team;
    if (index === 4) link.textContent = selectedLang.nav.contact;
  });

  // Update hero section
  document.querySelectorAll(".hero-content h1").forEach((el) => {
    el.innerHTML = selectedLang.hero.title;
  });
  document.querySelectorAll(".hero-content p").forEach((el) => {
    el.textContent = selectedLang.hero.subtitle;
  });
  document.querySelectorAll(".hero-btn .btn-default").forEach((el) => {
    el.textContent = selectedLang.hero.button;
  });

  // Update about section
  document.querySelector(".about-us-content h2").innerHTML =
    selectedLang.about.title;
  document.querySelector(".about-us-body p").textContent =
    selectedLang.about.content;
  document.querySelector(".about-us-btn .btn-default").textContent =
    selectedLang.about.button;
  document.querySelector(".about-experience-content").innerHTML =
    selectedLang.about.aboutExperienceContent;
  document.querySelector(".section-title .wow").innerHTML =
    selectedLang.about.sectionTitleWow;
  document.querySelectorAll(".about-list-content h3").forEach((item, index) => {
    item.textContent = selectedLang.about.features[index];
  });

  // Update services section
  document.querySelector(".our-services h2").innerHTML =
    selectedLang.services.title;
  document.querySelector(".section-btn .btn-default").textContent =
    selectedLang.services.button;
  document.querySelector(".section-title .wow.servicess").innerHTML =
    selectedLang.services.sectionTitleWowServicess;
  document.querySelectorAll(".service-item h3").forEach((item, index) => {
    item.textContent = selectedLang.services.items[index].title;
  });
  document.querySelectorAll(".service-item p").forEach((item, index) => {
    item.textContent = selectedLang.services.items[index].content;
  });
  document.querySelectorAll(".readmore-btn").forEach((item, index) => {
    item.textContent = selectedLang.services.items[index].readmoreBtn;
  });
  document.querySelector(".service-footer p").innerHTML =
    selectedLang.services.footer;

  // Update clients section
  document.querySelector(".our-brand-contant h2").innerHTML =
    selectedLang.clients.title;
  document.querySelector(".our-brand-btn .btn-default").textContent =
    document.querySelector(".testimonial .btn-default").textContent =
      selectedLang.clients.button;
  document.querySelector(".section-title .teamTitiel").textContent =
    selectedLang.clients.sectionTitleTema;
  document.querySelectorAll(".brand-item-content h3").forEach((item, index) => {
    item.textContent = selectedLang.clients.items[index].title;
  });
  document.querySelectorAll(".brand-item-content p").forEach((item, index) => {
    item.textContent = selectedLang.clients.items[index].content;
  });

  // Update team section
  const teamSection = document.querySelector(".page-team");
  if (teamSection) {
    teamSection.querySelector(".teamm h2").innerHTML = selectedLang.team.title;
    const teamHeading = teamSection.querySelector(".section-title h3");
    if (teamHeading) {
      teamHeading.textContent = selectedLang.team.heading;
    }

    // Update team members
    const teamItems = teamSection.querySelectorAll(".team-item");
    teamItems.forEach((item, index) => {
      if (selectedLang.team.members[index]) {
        const nameElement = item.querySelector(".team-content h3 a");
        const titleElement = item.querySelector(".team-content p");
        if (nameElement) {
          nameElement.textContent = selectedLang.team.members[index].name;
        }
        if (titleElement) {
          titleElement.textContent = selectedLang.team.members[index].title;
        }
      }
    });
  }

  // Update testimonials section
  const testimonialSection = document.querySelector(".testimonial");
  if (testimonialSection) {
    testimonialSection.querySelector("h2").innerHTML =
      selectedLang.testimonials.title;

    const testimonialHeading =
      testimonialSection.querySelector(".section-title h3");
    if (testimonialHeading) {
      testimonialHeading.textContent = selectedLang.testimonials.heading;
    }

    const testimonialBtn = testimonialSection.querySelector(
      ".section-btn .btn-default"
    );
    if (testimonialBtn) {
      testimonialBtn.textContent = selectedLang.testimonials.button;
    }

    document
      .querySelectorAll(".testimonial-content p")
      .forEach((item, index) => {
        item.textContent = selectedLang.testimonials.items[index].content;
      });

    document.querySelectorAll(".author-content h3").forEach((item, index) => {
      item.textContent = selectedLang.testimonials.items[index].author;
    });
    document
      .querySelectorAll(".testimonial-counter p")
      .forEach((item, index) => {
        item.textContent =
          selectedLang.testimonials.items[index].testimonialCounter;
      });
  }

  // Update footer
  document.querySelector(".footer-newsletter-title h2").textContent =
    selectedLang.footer.title;
  document.querySelectorAll(".footer-links h3")[0].textContent =
    selectedLang.footer.quickLinks;
  document.querySelectorAll(".footer-links h3")[1].textContent =
    selectedLang.footer.workHours;
  document.querySelectorAll(".footer-links h3")[2].textContent =
    selectedLang.footer.contact;
  document.querySelector(".footer-links.four a").textContent =
    selectedLang.footer.address;

  // Set checkbox state
  langToggle.checked = savedLang === "en";
  updatePageLanguage(savedLang);
};

// Function to update page language and direction
const updatePageLanguage = (lang) => {
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.title =
    lang === "ar"
      ? "توبان – محامون ومستشارون قانونيون"
      : "Toban - Lawyers and Legal Consultants";
};

// Event listener for language toggle
document
  .getElementById("language-toggle")
  .addEventListener("change", function () {
    const lang = this.checked ? "en" : "ar";
    localStorage.setItem("selectedLang", lang);
    loadLanguage();
    // location.reload(); // Removed to avoid reloading, as dynamic updates are handled
  });

// Initialize language on page load
document.addEventListener("DOMContentLoaded", loadLanguage);