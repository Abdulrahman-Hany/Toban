const updateApproachSection = (lang) => {
  const approach = translations[lang].approach;
  
  // Update approach section
  document.querySelector(".our-approach h3").textContent = approach.clients;
  document.querySelector(".our-approach h2").innerHTML = approach.trust;
  document.querySelector(".our-approach p").textContent = approach.description;
  
  // Update vision items
  const visionItems = document.querySelectorAll(".our-approach-item h3");
  visionItems.forEach(item => {
    item.textContent = approach.vision;
  });
  
  const visionTexts = document.querySelectorAll(".our-approach-item p");
  visionTexts[0].textContent = approach.visionText1;
  visionTexts[1].textContent = approach.visionText2;
  
  // Update FAQs section
  document.querySelector(".our-faqs h3").textContent = approach.faqTitle;
  document.querySelector(".our-faqs h2").innerHTML = approach.faqSubtitle;
  document.querySelector(".our-faqs p").textContent = approach.faqDescription;
  document.querySelector(".satisfy-client-content p").textContent = approach.clientTestimonials;
  
  // Update FAQ items
  const faqButtons = document.querySelectorAll(".accordion-button");
  const faqAnswers = document.querySelectorAll(".accordion-body p");
  
  approach.questions.forEach((question, index) => {
    faqButtons[index].textContent = question;
  });
  
  approach.answers.forEach((answer, index) => {
    faqAnswers[index].textContent = answer;
  });
};

// Modify the existing loadLanguage function to include approach section
const loadLanguage = () => {
  const savedLang = localStorage.getItem("selectedLang") || "ar";
  const selectedLang = translations[savedLang];
  const langToggle = document.getElementById("language-toggle");

  // Update navigation
  document.querySelectorAll(".navbar-nav .nav-link").forEach((link, index) => {
    if (index === 0) link.textContent = selectedLang.nav.home;
    if (index === 1) link.textContent = selectedLang.nav.about;
    if (index === 2) link.textContent = selectedLang.nav.services;
    if (index === 3) link.textContent = selectedLang.nav.team;
    if (index === 4) link.textContent = selectedLang.nav.contact;
  });
  
  document.querySelectorAll(".footer-links ul li a").forEach((link, index) => {
    if (index === 0) link.textContent = selectedLang.nav.home;
    if (index === 1) link.textContent = selectedLang.nav.about;
    if (index === 2) link.textContent = selectedLang.nav.services;
    if (index === 3) link.textContent = selectedLang.nav.team;
    if (index === 4) link.textContent = selectedLang.nav.contact;
  });
  
  document.querySelector(".breadcrumb .breadcrumb-item a").textContent = selectedLang.nav.home;

  // Update about section
  document.querySelector(".about-us-content h2").innerHTML = selectedLang.about.title;
  document.querySelector(".header-animer-about").innerHTML = selectedLang.headerAnimerAbout;
  document.querySelector(".about-us-body p").textContent = selectedLang.about.content;
  document.querySelector(".about-us-btn .btn-default").textContent = selectedLang.about.button;
  document.querySelector(".breadcrumb .about-header-pox").textContent = selectedLang.aboutHeaderPox;
  document.querySelector(".about-experience-content").innerHTML = selectedLang.about.aboutExperienceContent;
  document.querySelector(".section-title .wow").innerHTML = selectedLang.about.sectionTitleWow;
  
  document.querySelectorAll(".about-list-content h3").forEach((item, index) => {
    item.textContent = selectedLang.about.features[index];
  });
  
  // Update footer
  document.querySelector(".footer-newsletter-title h2").textContent = selectedLang.footer.title;
  document.querySelectorAll(".footer-links h3")[0].textContent = selectedLang.footer.quickLinks;
  document.querySelectorAll(".footer-links h3")[1].textContent = selectedLang.footer.workHours;
  document.querySelectorAll(".footer-links h3")[2].textContent = selectedLang.footer.contact;
  document.querySelector(".footer-links.four a").textContent = selectedLang.footer.address;
  
  // Update approach and FAQ sections
  updateApproachSection(savedLang);
  
  // Set checkbox state
  langToggle.checked = savedLang === "en";
  updatePageLanguage(savedLang);
};

// Keep the existing updatePageLanguage and event listener functions
const updatePageLanguage = (lang) => {
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.title = lang === "ar" 
    ? "توبان – محامون ومستشارون قانونيون" 
    : "Toban - Lawyers and Legal Consultants";
};

document.getElementById("language-toggle").addEventListener("change", function() {
  const lang = this.checked ? "en" : "ar";
  localStorage.setItem("selectedLang", lang);
  loadLanguage();
  location.reload();
});

// Initialize language on page load
document.addEventListener("DOMContentLoaded", loadLanguage);